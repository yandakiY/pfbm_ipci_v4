package com.pfbm.beans;

public class Resultat {
	private int id;
	private String id_resultat;
	private int valeur_ct;
	private String status_resultat;
	private String observation_resultat;
	private String id_analyse;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getId_resultat() {
		return id_resultat;
	}
	public void setId_resultat(String id_resultat) {
		this.id_resultat = id_resultat;
	}
	public int getValeur_ct() {
		return valeur_ct;
	}
	public void setValeur_ct(int valeur_ct) {
		this.valeur_ct = valeur_ct;
	}
	public String getStatus_resultat() {
		return status_resultat;
	}
	public void setStatus_resultat(String status_resultat) {
		this.status_resultat = status_resultat;
	}
	public String getObservation_resultat() {
		return observation_resultat;
	}
	public void setObservation_resultat(String observation_resultat) {
		this.observation_resultat = observation_resultat;
	}
	public String getId_analyse() {
		return id_analyse;
	}
	public void setId_analyse(String id_analyse) {
		this.id_analyse = id_analyse;
	}
	
	
}
