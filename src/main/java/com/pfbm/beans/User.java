package com.pfbm.beans;

public class User {
	public String id_user;
	public String nom_user;
	public String prenom_user;
	public String login_user;
	public String mdp_user;
	public String id_type_user;
	
	public String getId_user() {
		return id_user;
	}
	public void setId_user(String id_user) {
		this.id_user = id_user;
	}
	public String getNom_user() {
		return nom_user;
	}
	public void setNom_user(String nom_user) {
		this.nom_user = nom_user;
	}
	public String getPrenom_user() {
		return prenom_user;
	}
	public void setPrenom_user(String prenom_user) {
		this.prenom_user = prenom_user;
	}
	public String getLogin_user() {
		return login_user;
	}
	public void setLogin_user(String login_user) {
		this.login_user = login_user;
	}
	public String getMdp_user() {
		return mdp_user;
	}
	public void setMdp_user(String mdp_user) {
		this.mdp_user = mdp_user;
	}
	public String getId_type_user() {
		return id_type_user;
	}
	public void setId_type_user(String id_type_user) {
		this.id_type_user = id_type_user;
	}
	
	
}
