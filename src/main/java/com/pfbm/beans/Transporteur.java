package com.pfbm.beans;

public class Transporteur {
	private String id_transporteur;
	private String nom_transporteur;
	private String prenom_transporteur;
	private String sexe_transporteur;
	private String contact_transporteur;
	public String getId_transporteur() {
		return id_transporteur;
	}
	public void setId_transporteur(String id_transporteur) {
		this.id_transporteur = id_transporteur;
	}
	public String getNom_transporteur() {
		return nom_transporteur;
	}
	public void setNom_transporteur(String nom_transporteur) {
		this.nom_transporteur = nom_transporteur;
	}
	public String getPrenom_transporteur() {
		return prenom_transporteur;
	}
	public void setPrenom_transporteur(String prenom_transporteur) {
		this.prenom_transporteur = prenom_transporteur;
	}
	public String getSexe_transporteur() {
		return sexe_transporteur;
	}
	public void setSexe_transporteur(String sexe_transporteur) {
		this.sexe_transporteur = sexe_transporteur;
	}
	public String getContact_transporteur() {
		return contact_transporteur;
	}
	public void setContact_transporteur(String contact_transporteur) {
		this.contact_transporteur = contact_transporteur;
	}
	
	
}
